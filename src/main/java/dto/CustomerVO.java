package dto;

public class CustomerVO {
	
	private int customer_id;
	private String customer_company;
	private String customer_department;
	private String customer_manager;
	private String customer_email;
	private String customer_contact;
	
	
	public int getCustomer_id() {
		return customer_id;
	}
	public void setCustomer_id(int customer_id) {
		this.customer_id = customer_id;
	}
	public String getCustomer_company() {
		return customer_company;
	}
	public void setCustomer_company(String customer_company) {
		this.customer_company = customer_company;
	}
	public String getCustomer_department() {
		return customer_department;
	}
	public void setCustomer_department(String customer_department) {
		this.customer_department = customer_department;
	}
	public String getCustomer_manager() {
		return customer_manager;
	}
	public void setCustomer_manager(String customer_manager) {
		this.customer_manager = customer_manager;
	}
	public String getCustomer_email() {
		return customer_email;
	}
	public void setCustomer_email(String customer_email) {
		this.customer_email = customer_email;
	}
	public String getCustomer_contact() {
		return customer_contact;
	}
	public void setCustomer_contact(String customer_contact) {
		this.customer_contact = customer_contact;
	}
	
	

}
